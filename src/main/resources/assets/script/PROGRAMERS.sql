                    DURATION_TYPE,
                    IF(PLAN_ID IS NOT NULL,
                       FLOOR((DATEDIFF(END_DATE, START_DATE) + 1) * (DAILY_FEE - DAILY_FEE * DISCOUNT_RATE / 100)),
                       DAILY_FEE * (DATEDIFF(END_DATE, START_DATE) + 1))                                                 FEE,
                    ROW_NUMBER() OVER (PARTITION BY HISTORY_ID ORDER BY IF(PLAN_ID IS NOT NULL,
                                                                           FLOOR((DATEDIFF(END_DATE, START_DATE) + 1) *
                                                                                 (DAILY_FEE - DAILY_FEE * DISCOUNT_RATE / 100)),
                                                                           DAILY_FEE *
                                                                           (DATEDIFF(END_DATE, START_DATE) + 1)) ASC) AS RAN
             FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY HISTORY
                      INNER JOIN CAR_RENTAL_COMPANY_CAR CAR
                                 ON CAR.CAR_ID = HISTORY.CAR_ID
                                     AND CAR.CAR_TYPE = '트럭'
                      LEFT OUTER JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN PLAN
                                      ON CAR.CAR_TYPE = PLAN.CAR_TYPE
                                          AND REPLACE(DURATION_TYPE, '일 이상', '') <= DATEDIFF(END_DATE, START_DATE) + 1
             ORDER BY FEE DESC, HISTORY_ID DESC)
SELECT HISTORY_ID, FEE
FROM SUB
WHERE RAN = 1;

