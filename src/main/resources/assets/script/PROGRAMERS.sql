SELECT CAR.CAR_ID,
       CAR.CAR_TYPE,
       FLOOR(30 * (CAR.DAILY_FEE - (CAR.DAILY_FEE * PLAN.DISCOUNT_RATE / 100))) AS FEE
FROM CAR_RENTAL_COMPANY_CAR CAR
         LEFT OUTER JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY HISTORY
                         ON CAR.CAR_ID = HISTORY.CAR_ID
                             AND '202211' BETWEEN EXTRACT(YEAR_MONTH FROM START_DATE)
                                AND EXTRACT(YEAR_MONTH FROM END_DATE)
         INNER JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN PLAN
                    ON CAR.CAR_TYPE = PLAN.CAR_TYPE
                        AND DURATION_TYPE = '30일 이상'
WHERE HISTORY.HISTORY_ID IS NULL
  AND CAR.CAR_TYPE IN ('SUV', '세단')
  AND FLOOR(30 * (CAR.DAILY_FEE - (CAR.DAILY_FEE * PLAN.DISCOUNT_RATE / 100))) >= 500000
  AND FLOOR(30 * (CAR.DAILY_FEE - (CAR.DAILY_FEE * PLAN.DISCOUNT_RATE / 100))) < 2000000
ORDER BY FEE DESC,
         CAR.CAR_TYPE, CAR.CAR_ID DESC;



